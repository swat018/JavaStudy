package com.godofjava.chapter5;

public class OperatorRelational {
	public static void main(String[] args) {
		OperatorRelational operlator = new OperatorRelational();
		operlator.relational();
	}
	public void relational() {
		int intValue1 = 1;
		int intValue2 = 2;
		System.out.println(intValue1>intValue2);
		System.out.println(intValue1<intValue2);
		System.out.println(intValue1>=intValue2);
		System.out.println(intValue1<=intValue2);
		
	}

}
